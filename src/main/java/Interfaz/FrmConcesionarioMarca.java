    private void popEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_popEliminarActionPerformed

        try {
            //obtengo el item seleccionado en el comboConcesionario y lo asigno a conce 
            this.conce = (Concesionario) comboConcesionario.getSelectedItem();
            //obtengo el item seleccionado en el comboMarca y lo asigno a marca
            this.marca = (Marca) comboMarca.getSelectedItem();
            //llamo al metodo y le paos los parametros
            conceDAO.eliminarMarcaConcesionario(conce.getIdConcesionario(), marca.getIdMarca());
            //muestro los datos de la tabla dependiendo del ID seleccionado del combo
            mostrarTablaMarcas(((Concesionario) comboConcesionario.getSelectedItem()).getIdConcesionario());
            //muestro el mensaje en caso de que se haya ejecutado correctamente
            JOptionPane.showMessageDialog(null, "Datos eliminados con exito!!!!");
        } catch (Exception e) {
            //muestro el mensaje en caso de que no se haya ejecutado correctamente
            JOptionPane.showMessageDialog(null, "Error al eliminar los datos!!!!");
            //Muestro el Error
            System.err.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_popEliminarActionPerformed

