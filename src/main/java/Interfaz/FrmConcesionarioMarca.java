    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        try {
            //obtengo el item(ID) seleccionado del combo Concesionario
            this.conce = (Concesionario) comboConcesionario.getSelectedItem();
            //obtengo el item(ID) seleccionado del combo Marca
            this.marca = (Marca) comboMarca.getSelectedItem();

            int nMarca = conceDAO.getExisteMarcaConce(conce.getIdConcesionario(), marca.getIdMarca());

            if (nMarca < 1) {

                //llamo al metodo y obtengo los parametros
                conceDAO.agregarConcesionarioMarca(conce.getIdConcesionario(), marca.getIdMarca());
                //muestro la tabla para que se actualicen los datos
                mostrarTablaMarcas(((Concesionario) comboConcesionario.getSelectedItem()).getIdConcesionario());
                //muestro el mensaje de que se ejecuto correcto
                JOptionPane.showMessageDialog(null, "Datos guardados con exito!!!!");

            } else {
                
                JOptionPane.showMessageDialog(null, "Esta Marca ya est叩 registrada en el Concesionario");

            }

        } catch (Exception e) {
            //muestro el mensaje en caso de que no se haya ejecutado correctamente 
            JOptionPane.showMessageDialog(null, "Error al agregar!!!");
            //se muestra el error en el output
            System.err.println("Error!!! " + e.getMessage());
        }

    }//GEN-LAST:event_jButton3ActionPerformed

