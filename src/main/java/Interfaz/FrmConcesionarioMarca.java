    public void mostrarTablaMarcas(int pIdConcesionario) {

        //se crea un nuevo objeto de tipo DefaultTableModel
        DefaultTableModel modelo = new DefaultTableModel();

        //a単ado las columnas al modelo
        modelo.addColumn("IDMARCA");
        modelo.addColumn("MARCA");

        //Agrego el modelo a la tabla
        tablaConcesionarioMarca.setModel(modelo);

        String datos[] = new String[2];

        try {
            //Creo una nueva lista de tipo Marca
            List<Marca> lstMarca = conceDAO.listarMarcas(pIdConcesionario);

            //se recorre la lista para obtener los valores de la lista 
            lstMarca.forEach(m -> {

                //se obtienen los valores de la lista 
                datos[0] = String.valueOf(m.getIdMarca());
                datos[1] = m.getNombre();

                //se a単aden los datos al modelo
                modelo.addRow(datos);
            });

        } catch (Exception e) {
            //muestro mensaje de error en caso de que no se ejecute correctamente            
            System.out.println("Error al obtener datos " + e.getMessage());
            //se muestra el mensaje de que a ocurrido un error al llenat la tabla
            JOptionPane.showMessageDialog(null, "Error en el llenado de la tabla");
        }
    }

